
def add(a,b):
    c = a + b
    print("Adding 2 numbers:", c)
    return c

def sub(a,b):
    c = a - b
    print("Sub of 2 numbers:", c)
    return c

def mul(a,b):
    c = a * b
    print("Mul of 2 numbers:", c)
    return c

def div(a,b):
    c = a / b
    print("Div of 2 numbers:", c)
    return c

g = add(15,25)
h = sub(20,10)
i = mul(20,20)

print(g + h + i)